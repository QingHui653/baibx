package com.hengshuo.baibx.model;

/**
 * Information entity. @author MyEclipse Persistence Tools
 */

public class Information implements java.io.Serializable {

	// Fields

	private Integer id;
	private String type;
	private String title;
	private String file;
	private String content;
	private String time;
	private String referee;
	private String top;
	private Integer clicks;

	// Constructors

	/** default constructor */
	public Information() {
	}

	/** full constructor */
	public Information(String type, String title, String file, String content,
			String time, String referee, String top, Integer clicks) {
		this.type = type;
		this.title = title;
		this.file = file;
		this.content = content;
		this.time = time;
		this.referee = referee;
		this.top = top;
		this.clicks = clicks;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getType() {
		return this.type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTitle() {
		return this.title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getFile() {
		return this.file;
	}

	public void setFile(String file) {
		this.file = file;
	}

	public String getContent() {
		return this.content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTime() {
		return this.time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getReferee() {
		return this.referee;
	}

	public void setReferee(String referee) {
		this.referee = referee;
	}

	public String getTop() {
		return this.top;
	}

	public void setTop(String top) {
		this.top = top;
	}

	public Integer getClicks() {
		return this.clicks;
	}

	public void setClicks(Integer clicks) {
		this.clicks = clicks;
	}

}